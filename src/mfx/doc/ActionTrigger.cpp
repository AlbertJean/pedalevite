const char *  ActionTrigger_get_name (ActionTrigger trig)
{
	assert (trig >= 0);
	assert (trig < ActionTrigger_NBR_ELT);

	static const std::array <
		const char *,
		ActionTrigger_NBR_ELT
	>	name_0_arr =
	{{
		"Press", "Hold", "Release"
	}};

	return name_0_arr [trig];
}



